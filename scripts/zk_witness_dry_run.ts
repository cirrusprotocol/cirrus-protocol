#!/usr/bin/env ts-node
//
// WITHDRAW_SOL_V1 witness dry run
//
// Compiles the circuit to /tmp, builds a deterministic dummy input using the
// existing witness helpers, runs generate_witness.js, and checks the witness
// against the R1CS with snarkjs wtns check.
//
// This script stops at snarkjs wtns check. It does NOT run:
//   - snarkjs powersoftau
//   - snarkjs groth16 setup
//   - snarkjs zkey / snarkjs zkey contribute / snarkjs zkey export verificationkey
//   - snarkjs groth16 prove
//   - snarkjs wtns export json
//
// No generated artifact (.r1cs, .wasm, .sym, .wtns, input.json, snapshot.json)
// is written into the repo. Everything stays under /tmp.
//
// SECURITY NOTES:
//   - TEST_SECRET is deterministic test data only. It is not a real user secret.
//   - The generated input.json contains TEST_SECRET as a decimal string.
//     It must not be committed. It lives under /tmp only.
//   - witness.wtns is private witness data. It must not be committed.
//     It lives under /tmp only.
//   - No proof, VK, zkey, or ptau is generated by this script.
//
// Usage:
//   npm run zk:witness-dry-run
//   npm run zk:witness-dry-run -- --keep-tmp
//   npm run zk:witness-dry-run -- --skip-npm-install
//   npm run zk:witness-dry-run -- --require-clean

